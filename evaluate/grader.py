def extract_boxed_answer(machine_generated_answer: str) -> str:
    """
    从机器生成的答案中提取boxed部分
    :param machine_generated_answer: 机器生成的答案字符串
    :return: 提取的boxed部分，如果没有则返回空字符串
    """
    # 正则表达式匹配 \boxed{...} 部分
    pattern = re.compile(r'\\boxed\{(.*?)\}')
    match = pattern.findall(machine_generated_answer)
    if match:
        # return match.group(1)
        return match[-1]
    return ""

